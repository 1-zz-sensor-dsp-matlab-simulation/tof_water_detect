%****************************************************************************************
% FIR低通滤波器设计
%***************************************************************************************
fs=1000; %设置采样频率 1K
N=1024; %采样点数
n=0:N-1;
t=n/fs; %时间序列
f=n*fs/N; %频率序列

x=sin(2*pi*50*t)+sin(2*pi*200*t); %50Hz和200Hz正弦波混合
b=fir1(28, 0.25);
y=filter(b, 1, x);
subplot(211);
plot(t, y);
title('Matlab FIR滤波后的波形');
grid on;